import { Schema, model } from "mongoose"

const USER_SCHEMA = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    default: "user",
  },
})

const User = model("User", USER_SCHEMA)
export default User
